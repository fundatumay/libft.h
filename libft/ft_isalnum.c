#include "libft.h"

int	isalnum(int c)
{
	if ((c <= 'z' && c >= 'a') || (c <= 'Z' && c >= 'A') || (c >= '0' && c <= '9'))
		return (1);
	return (0);
}

int	main(void)
{
	int	c;
	c = ',';
	printf("%d", isalnum(c));
}

